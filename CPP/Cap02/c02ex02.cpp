#include <iostream>
#include <cmath>

using namespace std;

int main(void) {
  // Funções matemáticas
  cout << cos(1) << endl;   // Saída: 0.54030...
  cout << exp(1) << endl;   // Saída: 2.71828...
  cout << log(2) << endl;   // Saída: 0.69314...
  cout << sin(1) << endl;   // Saída: 0.84147...
  cout << sqrt(2) << endl;  // Saída: 1.41421...

  return 0;
}
