package main

import (
  "fmt"
  "math"
)

func main() {
  // Funções matemáticas
  fmt.Println(math.Cos(1))   // Saída: 0.54030...
  fmt.Println(math.Exp(1))   // Saída: 2.71828...
  fmt.Println(math.Log(2))   // Saída: 0.69314...
  fmt.Println(math.Sin(1))   // Saída: 0.84147...
  fmt.Println(math.Sqrt(2))  // Saída: 1.41421.....
}
