public class Main {
    public static void main(String[] args) {
        // Operadores binários
        System.out.println(5 + 3);    // Saída: 8
        System.out.println(10 - 4);   // Saída: 6
        System.out.println(2 * 6);    // Saída: 12
        System.out.println(10.0 / 3); // Saída: 3.333... (quocien-te real)
        System.out.println(10 / 3);   // Saída: 3 (quociente in-teiro)
        System.out.println(10 % 3);   // Saída: 1 (resto da divi-são)

        // Operadores unários
        System.out.println(+5);       // Saída: 5 (mantém positi-vo)
        System.out.println(-8);       // Saída: -8 (inverte sinal)

        // Exponenciação
        System.out.println(Math.pow(2, 3)); // Saída: 8.0 (2 ele-vado a 3)
    }
}
