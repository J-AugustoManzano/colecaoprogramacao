import 'dart:math';

void main() {
  // Operadores binários
  print(5 + 3);     // Saída: 8
  print(10 - 4);    // Saída: 6
  print(2 * 6);     // Saída: 12
  print(10.0 / 3);  // Saída: 3.3333... (quociente real)
  print(10 ~/ 3);   // Saída: 3 (quociente inteiro)
  print(10 % 3);    // Saída: 1 (resto da divisão)
  // Operador unário
  print(-8);        // Saída: -8 (inverte sinal)
  // Exponenciação
  print(pow(2.0, 3.0)); // Saída: 8.0 (2 elevado a 3)
}
