#include <stdio.h>
#include <math.h>

int main(void) {
  // Funções matemáticas
  printf("%.5f\n", cos(1));   // Saída: 0.54030...
  printf("%.5f\n", exp(1));   // Saída: 2.71828...
  printf("%.5f\n", log(2));   // Saída: 0.69314...
  printf("%.5f\n", sin(1));   // Saída: 0.84147...
  printf("%.5f\n", sqrt(2));  // Saída: 1.41421...

  return 0;
}
