#!/usr/bin/env ruby

# Operadores binários

puts 5 + 3     # Saída: 8
puts 10 - 4    # Saída: 6
puts 2 * 6     # Saída: 12
puts 10 / 3.0  # Saída: 3.3333333333333335 (quociente real)
puts (10 / 3)  # Saída: 3 (quociente inteiro)
puts 10 % 3    # Saída: 1 (resto da divisão)

# Operadores unários

puts +5       # Saída: 5 (mantém sinal posit.)
puts -8       # Saída: -8 (inverte sinal negat.)

# Exponenciação

puts 2 ** 3   # Saída: 8 (2 elevado a 3)
