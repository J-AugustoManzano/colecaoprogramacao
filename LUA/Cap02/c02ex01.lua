-- Operadores binários

print(5 + 3)     -- Saída: 8
print(10 - 4)    -- Saída: 6
print(2 * 6)     -- Saída: 12
print(10 / 3.0)  -- Saída: 3.3333333333333335 (quociente real)
print(10 // 3)   -- Saída: 3 (quociente inteiro)
print(10 % 3)    -- Saída: 1 (resto da divisão)

-- Operadores unários

print(-8)       -- Saída: -8 (inverte sinal negativo)

-- Exponenciação

print(2 ^ 3)    -- Saída: 8 (2 elevado a 3)
