// Operadores binários

printf("%d\n", 5 + 3);         // Saída: 8
printf("%d\n", 10 - 4);        // Saída: 6
printf("%d\n", 2 * 6);         // Saída: 12
printf("%.4f\n", 10.0 / 3);    // Saída: 3.3333 (quociente real)
printf("%d\n", floor(10 / 3)); // Saída: 3 (quociente inteiro)
printf("%d\n", 10 % 3);        // Saída: 1 (resto da divisão)

// Operadores unários

printf("%d\n", +5);            // Saída: 5 (mantém positivo)
printf("%d\n", -8);            // Saída: -8 (inverte sinal)

// Exponenciação

printf("%d\n", pow(2, 3));     // Saída: 8 (2 elevado a 3)

?>
