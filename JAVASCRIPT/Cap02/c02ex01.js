// Operadores binários

console.log(5 + 3);                 // Saída: 8
console.log(10 - 4);                // Saída: 6
console.log(2 * 6);                 // Saída: 12
console.log((10.0 / 3).toFixed(4)); // Saída: 3.3333 (quociente real)
console.log(Math.floor(10 / 3));    // Saída: 3 (quociente inteiro)
console.log(10 % 3);                // Saída: 1 (resto da divisão)

// Operadores unários

console.log(+5); // Saída: 5 (mantém positivo)
console.log(-8); // Saída: -8 (inverte sinal)

// Exponenciação

console.log(Math.pow(2, 3)); // Saída: 8 (2 elevado a 3)
